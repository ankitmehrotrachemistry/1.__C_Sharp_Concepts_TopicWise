using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Data_Types_CSharp
{
    class Program
    {
        static void Main(string[] args)
        {
            int iNum = 40;
            double doubleNum = 42.44D;
            char myChar = 'A';
            long lNum = 1400000000L;
            float fNum = 54.78f;

            Console.WriteLine("Integer Number is : " +iNum);
            Console.WriteLine("Double Number is : " +doubleNum);
            Console.WriteLine("Character is : " +myChar);
            Console.WriteLine("Long Number is : " +lNum);
            Console.WriteLine("Float Number is : " +fNum);
        }
    }
}
